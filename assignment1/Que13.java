/*
13. The area of a right triangle is 184 cm² and one of its legs is 16 cm long.
 Find the length of other leg.
 */
class Que13{
    public static void main(String args[]){
        int area=184;
       int width=16;
       int length;
      // area=1/2*w*l
      length=(2*area)/width;
      System.out.println("length of other leg is:"+length);


    }
}